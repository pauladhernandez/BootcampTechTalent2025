
public class T4_Ejercicio2 {

	public static void main(String[] args) {
//		Declaramos las variables a cada tipo (int, double, char)
		int n=20;
		double a=10.5;
		char letra='p';
		
//		Operaciones matemáticas de las variables
		double suma = n + a;
		double resta = a - n;
		int valorNumerico = (int) letra;
		
//		Resultados
		System.out.println("Variable N = 20");
		System.out.println("Variable A = 10.5");
		System.out.println("Variable C = p");
		System.out.println("20 + 10.5 = " + suma);
		System.out.println("10.5 - 20 = " + resta);
		System.out.println("Valor numérico del carácter p: " + valorNumerico );
		
		
	}

}
